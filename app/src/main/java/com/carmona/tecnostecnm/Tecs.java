public class Tecs {
    private int idTecno;
    private String nombreTecno;
    private String nombreCortoTecno;
    private String logoTecno;

    public int getIdTecno() {
        return idTecno;
    }

    public void setIdTecno(int idTecno) {
        this.idTecno = idTecno;
    }

    public String getNombreTecno() {
        return nombreTecno;
    }

    public void setNombreTecno(String nombreTecno) {
        this.nombreTecno = nombreTecno;
    }

    public String getNombreCortoTecno() {
        return nombreCortoTecno;
    }

    public void setNombreCortoTecno(String nombreCortoTecno) {
        this.nombreCortoTecno = nombreCortoTecno;
    }

    public String getLogoTecno() {
        return logoTecno;
    }

    public void setLogoTecno(String logoTecno) {
        this.logoTecno = logoTecno;
    }


}
